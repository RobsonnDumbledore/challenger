package br.com.codart.model;

public class Average {

    private Double valueA;
    private Double valueB;

    public Average() {
    }

    public Average(Double valueA, Double valueB) {
        this.valueA = valueA;
        this.valueB = valueB;
    }

    public Double getValueA() {
        return valueA;
    }

    public Double getValueB() {
        return valueB;
    }

}
